package Lesson2;

public class home4 {
    public static void main(String[] args) {
        //2 урок 4 завданя. Напишіть програму для обчислення суми перших 10 натуральних чисел.
        int sum = 0;
        for (int x = 0; x < 10; x++) {
            sum += x;
            System.out.println("сума перших 10 числел :" + sum);
        }
    }
}
